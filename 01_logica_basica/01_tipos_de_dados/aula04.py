"""
Tipos int e float
int -> numero inteiro
o tipo int representa qualquer numero,sendo positivo ou neativo, lembrando que o se nao tiver sinal é positivo

float -> numero com ponto flutuante
representa qualquer numero positivo ou negativo de ponto flutunate, e se nao tiver o sinal é considerado positivo

funcao type mostra o tipo de dado que o python atribuiu aos valores apresentados, no caso é uma classe

"""
print("Tipo primitivo int:")
print(558) # int
print(-558) #int
print(0)

print("Tipo primitivo float:")
print(54.64) #float
print(-54.64) #float
print(0.0) #float

#tipo do valor
print(type(544))
print(type(5.44))
print(type('5.44'))
